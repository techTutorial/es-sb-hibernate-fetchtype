DROP TABLE IF EXISTS ES_PRODUCT;

create table ES_PRODUCT (
   ID_PRODUCT BIGINT AUTO_INCREMENT PRIMARY KEY,
   NAME_COMPANY varchar(100) NOT NULL
);



DROP TABLE IF EXISTS ES_ACCESSORIES;

create table ES_ACCESSORIES (
   ID_ACCESSORIES BIGINT AUTO_INCREMENT PRIMARY KEY,
   ID_PRODUCT BIGINT,
   NAME_ACCESSORIES varchar(100) NOT NULL,
   -- FOREIGN KEY definition is optional;
   FOREIGN KEY (ID_PRODUCT) REFERENCES ES_ACCESSORIES (ID_PRODUCT)
);

